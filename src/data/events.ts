function mkInstant(
  id: string, asset: string, icon: string, assetClass: 'crypto' | 'stocks' | 'commodities' | 'forex',
  current: number, strike: number, dir: 'UP' | 'DOWN', prob: number, vol: number,
  startOff: number, endOff: number
): PredictionEvent {
  const priceLabel = strike >= 1000 ? `$${strike.toLocaleString()}` : strike >= 1 ? `$${strike}` : `$${strike.toFixed(4)}`
  const cmp = dir === 'UP' ? 'above' : 'below'
  const catMap: Record<string, string> = { crypto: 'Crypto', stocks: 'Finance', commodities: 'Finance', forex: 'Finance' }
  return {
    id, type: 'instant', outcomeModel: 'independent',
    title: `${asset} ${cmp} ${priceLabel} in 5 min?`,
    description: `Predict if ${asset} price will be ${cmp} ${priceLabel} when this market closes in 5 minutes.`,
    category: catMap[assetClass],
    tags: ['instant', asset.toLowerCase().replace(/[^a-z]/g, ''), 'price'],
    status: 'OPEN',
    statusInfo: { status: 'OPEN', subStatus: 'normal', reason: '', updatedAt: new Date().toISOString() },
    rulesMeasurement: `${asset} spot price at expiry timestamp`,
    rulesClosing: 'Market closes exactly 5 minutes after open',
    resolutionSource: `${assetClass === 'crypto' ? 'Binance' : assetClass === 'forex' ? 'Forex API' : 'Market data API'} ${asset}`,
    timeline: { openDate: instantTime(startOff), closeDate: instantTime(endOff) },
    contracts: [{
      id: `ct-${id}-yn`, eventId: id,
      label: `${asset} ${dir === 'UP' ? '>' : '<'} ${priceLabel}`,
      status: 'OPEN', yesPrice: prob / 100, noPrice: 1 - prob / 100,
      probability: prob, change24h: 0, volume: vol,
      expiresAt: instantTime(endOff), payoutPerShare: 1,
    }],
    totalVolume: vol, featured: false,
    instant: {
      asset, assetIcon: icon, assetClass, currentPrice: current,
      strikePrice: strike, direction: dir, durationSeconds: 300,
      startedAt: instantTime(startOff), endsAt: instantTime(endOff),
    },
  }
}

