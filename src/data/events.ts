// Helper: get all non-sports events
export const standardEvents = events.filter(e => e.type !== 'sports')

// Helper: get all sports events
export const sportsEvents = events.filter(e => e.type === 'sports')

// Helper: get featured events
export const featuredEvents = events.filter(e => e.featured)

// Helper: get events by category
export function getEventsByCategory(category: string): PredictionEvent[] {
  if (category === 'All') return events.filter(e => e.category !== 'Sports')
  return events.filter(e => e.category === category)
}

// Helper: find event by id
export function getEventById(id: string): PredictionEvent | undefined {
  return events.find(e => e.id === id)
}

// Helper: find contract by id (searches across all events)
export function getContractById(contractId: string): { event: PredictionEvent; contract: import('../types').Contract } | undefined {
  for (const event of events) {
    const contract = event.contracts.find(c => c.id === contractId)
    if (contract) return { event, contract }
  }
  return undefined
}
