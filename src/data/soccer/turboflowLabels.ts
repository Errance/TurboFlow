export function getTurboFlowLabel(stakeTitle: string, home?: string, away?: string): string {
  const mapped = marketLabelMap[stakeTitle]
  if (mapped) {
    let result = mapped
    if (home) result = result.replace('主队', home)
    if (away) result = result.replace('客队', away)
    return result
  }

  if (stakeTitle.includes('不丢球获胜')) return `${stakeTitle.split('不丢球')[0].trim()}能否零封获胜？`
  if (stakeTitle.includes('不失球')) return `${stakeTitle.split('不失球')[0].trim()}能否零封？`
  if (stakeTitle.includes('赢任何半场')) return `${stakeTitle.split('赢')[0].trim()}能否赢下任一半场？`
  if (stakeTitle.includes('赢两半场')) return `${stakeTitle.split('赢')[0].trim()}能否两个半场都赢？`
  if (stakeTitle.includes('最高得分半场')) {
    const prefix = stakeTitle.replace(/最高得分半场.*/, '').trim()
    return prefix ? `${prefix}哪个半场进球更多？` : '哪个半场进球更多？'
  }
  if (stakeTitle.includes('奇/偶') || stakeTitle.includes('奇数/偶数')) return `${stakeTitle.replace(/奇.*/, '')}进球是奇数还是偶数？`
  if (stakeTitle.includes('两个半场得分')) return `${stakeTitle.split('两个')[0].trim()}能否两个半场都进球？`
  if (stakeTitle.includes('多进球')) {
    const prefix = stakeTitle.replace(/多进球.*/, '').trim()
    return prefix ? `${prefix}进球数在哪个范围？` : '总进球数会落在哪个范围？'
  }
  if (stakeTitle.includes('准确进球')) {
    const prefix = stakeTitle.replace(/准确进球.*/, '').trim()
    return prefix ? `${prefix}会有几个进球？` : '全场会有几个进球？'
  }
  if (stakeTitle.includes('合计')) return `${stakeTitle}是否超过指定数？`
  if (stakeTitle.includes('让球')) return `${stakeTitle}后谁的进球更多？`
  if (stakeTitle.includes('角球范围')) return `${stakeTitle.replace('角球范围', '')}角球数在什么区间？`
  if (stakeTitle.includes('正确角球数')) return `${stakeTitle.replace('正确角球数', '')}角球数是多少？`
  if (stakeTitle.includes('盘口')) return `${stakeTitle}让球后谁赢？`

  return stakeTitle
}
