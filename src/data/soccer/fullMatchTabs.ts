type BGOpt = [string, number]
const bg = (title: string, opts: BGOpt[]): Market => ({
  type: 'buttonGroup', title, options: opts.map(([label, odds]) => ({ label, odds })),
})
type OTRow = [string, ...number[]]
const ot = (title: string, columns: string[], rows: OTRow[]): Market => ({
  type: 'oddsTable', title, columns, rows: rows.map(([line, ...odds]) => ({ line, odds })),
})
const rb = (title: string, opts: BGOpt[]): Market => ({
  type: 'rangeButtons', title, options: opts.map(([label, odds]) => ({ label, odds })),
})
const sgm = (title: string, hr: number[], ar: number[], odds: Record<string, number>): Market => ({
  type: 'scoreGrid', title, homeRange: hr, awayRange: ar, odds,
})
type CGCell = [string, number]
const cgm = (title: string, rh: string[], ch: string[], cells: CGCell[]): Market => ({
  type: 'comboGrid', title, rowHeaders: rh, colHeaders: ch, cells: cells.map(([label, odds]) => ({ label, odds })),
})
const plm = (title: string, tiers: string[], players: { name: string; odds: number[] }[]): Market => ({
  type: 'playerList', title, tiers, players,
})

